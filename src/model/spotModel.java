package model;

public class spotModel {
	private Integer spotNum;
	private String spotName;
	private String spotCity;
	private String spotArea;
	private String spotType;
	private String spotOther;
	
	public spotModel() {
		super();
	}

	public Integer getSpotNum() {
		return spotNum;
	}
	public void setSpotNum(Integer spotNum) {
		this.spotNum = spotNum;
	}
	public String getSpotName() {
		return spotName;
	}
	public void setSpotName(String spotName) {
		this.spotName = spotName;
	}
	public String getSpotCity() {
		return spotCity;
	}
	public void setSpotCity(String spotCity) {
		this.spotCity = spotCity;
	}
	public String getSpotArea() {
		return spotArea;
	}
	public void setSpotArea(String spotArea) {
		this.spotArea = spotArea;
	}
	public String getSpotType() {
		return spotType;
	}
	public void setSpotType(String spotType) {
		this.spotType = spotType;
	}
	public String getSpotOther() {
		return spotOther;
	}
	public void setSpotOther(String spotOther) {
		this.spotOther = spotOther;
	}

	
	
	
	
}
